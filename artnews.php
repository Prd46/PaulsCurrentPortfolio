        <p class="freeform paragraph">In my search for a news site that I would be interested in working with, I came 
                across ARTnews, which is dedicated to updating the art connoisseurs of the world 
                on what’s happening in the art world. Unfortunately for them (and fortunately for 
                me), there were quite a number of issues I ran into while exploring the site that 
                could be innovated on. The search bar gives you two categories to search from, but 
                neither of them are clickable, and the navigation of the site is incredibly 
                confusing, some heading tabs leading you to other sites entirely. While I was not 
                able to address the search issue, since I hadn’t learned coding at the time, I was 
                able to greatly simplify the site navigation by prioritizing the main interests 
                of the company (gaining more subscriptions, since they advertised it heavily 
                throughout their own pages), while putting the other specifications in the menu. 
                The rest of the page displays the leading articles, editor picks, most recent 
                uploads and top stories, taking up the majority of the screen space with large and 
                attention-grabbing pictures.</p>
