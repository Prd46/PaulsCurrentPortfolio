<p class="freeform paragraph"> The Road by Cormac McCarthy is my favorite book, one of the few that I will avidly sit down to
    read on my own volition, and the movie is also quite good. The only issue is that the original 
    trailer did a horrible job of conveying the true nature of the story. It's a grim, yet realistic
    approach to what life would be like around the end of humanity, and you follow along with the boy
    and his father in their struggle for survival. With the two trailers of this movie that I saw, they
    try to either convey this movie as some action packed disaster film, or some light-hearted family
    story, with terribly unfitting music in both. What puzzles me the most is that the music in the
    film made for one of the most compelling soundtracks I have heard in cinema, and it was the original
    reason that I sought to read the book in the first place. As such, I thought it right to replace the
    audio with something truly representative of what the film makes you feel, using additional sound
    effects for tension. Apart from the fact that it sounds a bit weird with only my voice, this is one 
    of the projects that I feel most proud of overall. 
</p>
