<p class="freeform paragraph">For the record, this project was done in a group of four, so I cannot take credit for everything.
     The other three members of my group were Preeti Shenoy Lianna Wang, and Kara Butler. The reason
      why I am including it in my own portfolio is because I came up with a lot of the ideas to add 
      onto the final product.</p>

<p class="freeform paragraph">Unlike the Gryphon Glove project, another project for this class, we were tasked with actually designing a product that would solve 
    a problem that we would need to research. We had a lot of ideas from the get go, but our professor 
    reminded us not to jump too far ahead too early to make sure we could refine it enough. Right away,
     we decided on creating a device to give college students with ADHD or trouble focusing a means 
     of dealing with procrastination. Our team evenly split up the research and I was responsible for 
     the neuroscience perspective on ADHD and potential causes. Then, we had to come up with 20 design 
     ideas as a group, so we split it up evenly amongst the four of us. Many of ideas were very similar, 
     and the idea we decided to go through with would be a little helper friend that would help you 
     create and maintain schedules while giving encouragement towards your accomplishments.</p>

<p class="freeform paragraph">Surprisingly close to the end of the project, however, I had thought of a lot of ideas that would 
    make the design of our Study Buddy much more effective. I remembered that my step mom has ADHD and that 
    she has an emotional support dog that counts as a service animal. What's a better buddy than a 
    puppy? We had already thought of the schedule creation ideas that would be built into the device 
    and its mobile app counterpart, but we quickly found a way to incorporate the dog aesthetic into 
    the device. While the initial device would work on suggesting work times and giving reminders, 
    the Study Pup and the Study Pup app would do all that AND motivate the user to adhere to their 
    plans by directly connecting it to the dog theme. By completing your work on time, you can earn 
    virtual credits to buy digital treats and toys for the dog on the app, similar to a Tamagotchi. 
    Meanwhile, the physical robot dog would interact with the user, listening to what items they 
    need to add to your schedule, and because it looks a little cartoony, it's a talking dog that 
    can give advice and reminders back to the user.</p>

<p class="freeform paragraph">The most effective idea of mine, I believe, was to have the dog be powered by the user's phone, 
    which they would need to place inside a wireless charger "bed" that the puppy would sit in, 
    covering up the phone and preventing it from being used while they are working. This cuts out 
    a major distraction that has afflicted many students' attention spans, not just those with ADHD. 
    Certainly if this were a real product, there would be a manual override in case you needed to use 
    your phone in an emergency call. Otherwise, with this product, people could develop better study 
    habits and have fun doing so too, by taking care of their own little helper friend at the same time.</p>
