<h2>Wandering Regrets</h2>
<p class="freeform paragraph">The only photography class I’ve taken was taught in black and white, 
    so that is what I am most familiar with. That’s definitely not a 
    bad thing though, because I’ve gotten really good at it. I 
    particularly enjoy using the lack of color to put emphasis on other 
    design features of each photo, such as the geometry and framing, 
    which is the main variable in this set. I had gone out with my friend 
    Sydney to capture some, since she is taking the class this term and 
    I wanted to explore the limits of contrast with her. Despite the 
    melancholic demeanor of these few, we had a good time.</p>
<p class="freeform paragraph">
    That being said, the theming behind this little collection is based 
    on some thoughts that pass through my head at times, like weary 
    travelers looking for a place to stay for a while. I take them in 
    and turn them into something beautiful. It’s an interesting double 
    standard, how most of the joy in my life comes from my creative 
    outlets, photography included, yet my main inspiration is rooted 
    in despair. Perhaps it’s the thought that my free-flowing ambitions 
    aren’t enough on their own, as a career. “Visions Imprisoned,” 
    I feel, best captures this emotion, for my will to create will 
    need to be shelled up into the inflexible foundation of societal 
    worth, an artistic eye peering through metal bars. Enjoy.</p>   
