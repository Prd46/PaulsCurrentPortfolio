        <p class="freeform paragraph">The major difference between User Interface Design I and II was that our choice of 
            site to redesign was much more limited. While before we could choose whatever 
            site we wanted, we had to pick a type of site that was very content heavy, 
            specifically a news site, as directed by our instructor. Since I rarely pay 
            attention to the news, I was a bit reluctant to get started, but soon realized 
            that it’s essential that I need to be open to all types of sites and 
            configurations, since there are sites for almost anything, and I could be working 
            for any one of them someday as a user experience designer. It was a necessary pull 
            out of my comfort zone, and it gave me a huge confidence boost when the ideas 
            started flowing in.</p>
